    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        String nama = txtNama.getText();
        String email = txtEmail.getText();

        // Validasi input jumlah tiket
        int jumlahTiket;
        try {
            jumlahTiket = Integer.parseInt(txtTiket.getText());
        } catch (NumberFormatException e) {
            System.out.println("Masukkan jumlah tiket yang valid");
            return;
        }

        // Hitung total bayar (misalnya harga per tiket 50000)
        int hargaPerTiket = 50000;
        int totalBayar = jumlahTiket * hargaPerTiket;
        txtBayar.setText(String.valueOf(totalBayar));

        // Menyimpan data ke tabel - Pastikan urutan sesuai dengan kolom tabel
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // Cek kolom pada model tabel
        /* Kolom pada model tabel berdasarkan kode:
       "Nama", "Email", "Total Bayar", "Jml Tiket"
         */
        // Pastikan menambahkan data dalam urutan yang benar
        model.addRow(new Object[]{
            nama, // kolom 0: Nama
            email, // kolom 1: Email
            String.valueOf(totalBayar), // kolom 2: Total Bayar
            jumlahTiket // kolom 3: Jml Tiket
        });

        // Membersihkan input setelah pembayaran
        txtNama.setText("");
        txtEmail.setText("");
        txtNomor.setText("");
        txtTiket.setText("");
        txtBayar.setText("");
    }//GEN-LAST:event_btnBayarActionPerformed

