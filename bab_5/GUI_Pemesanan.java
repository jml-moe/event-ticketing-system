    // Metode Overriding untuk simpan data
    public void simpanData() {
        // Ambil data dari tabel
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Simulasikan penyimpanan data
        System.out.println("Data disimpan: ");
        for (int i = 0; i < model.getRowCount(); i++) {
            System.out.println("Nama: " + model.getValueAt(i, 0) + ", Email: " + model.getValueAt(i, 1) +
                    ", Metode Bayar: " + model.getValueAt(i, 2) + ", Jumlah Tiket: " + model.getValueAt(i, 3));
        }
    }

    // Metode Overloading untuk simpan data dengan parameter
    public void simpanData(String nama, String email, String metode, int jumlah) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{nama, email, metode, jumlah});
        System.out.println("Data disimpan: " + nama + ", " + email + ", " + metode + ", " + jumlah);
    }

    // Metode Overriding untuk hapus data
    public void hapusData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            model.removeRow(selectedRow);
            System.out.println("Data berhasil dihapus!");
        } else {
            System.out.println("Pilih baris yang akan dihapus.");
        }
    }

